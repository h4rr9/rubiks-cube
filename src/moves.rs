pub enum Moves {
    L,  // Clockwise Left turn
    R,  // Clockwise Right turn
    F,  // Clockwise Front turn
    B,  // Clockwise Back turn
    U,  // Clockwise Up turn
    D,  // Clockwise Down turn
    _L, // Anti-Clockwise Left turn
    _R, // Anti-Clockwise Right turn
    _F, // Anti-Clockwise Front turn
    _B, // Anti-Clockwise Back turn
    _U, // Anti-Clockwise Up turn
    _D, // Anti-lockwise Down turn
    L2, // Half Left turn
    R2, // Half Right turn
    F2, // Half Front turn
    B2, // Half Back turn
    U2, // Half Up turn
    D2, // Half Down turn
}
